def primeira_letra(texto):
    modificado = texto.capitalize()
    return modificado

def maiuscula(texto):
    modificado = texto.upper()
    return modificado

def toda_primeira_letra(texto):
    modificado = texto.title()
    return modificado

def minuscula(texto):
    modificado = texto.lower()
    return modificado

